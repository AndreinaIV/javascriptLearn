let i = 1000; //Inicio


do{
    console.log(`Número ${i}`)

    i++; //Incremento
} while ( i < 10 ); //Condición

//Diferencia entre while y do while, el do while se va a ejecutar al menos una vez, no importa si se cumple la condición y después comprueba. 
//El while verifica la condición antes de ejecutarla. 